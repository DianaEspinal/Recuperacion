                <p>Durante este taller, el MSc. Enrique nos fomentó el pensamiento crítico y el valor de tomar decisiones fundamentadas en datos previamente evaluados. 
                A través de una dinámica práctica, nos presentó un conjunto de datos completamente desordenados, simulando una situación real en la que la información no está estructurada ni categorizada.
                Con paciencia y método, procedió a organizar esos datos, aplicando filtros y segmentaciones por categorías relevantes, lo que permitió visualizar patrones y tendencias de manera clara.
                Este proceso no solo demostró la importancia de la limpieza y estructuración de datos, sino que también evidenció cómo estas acciones pueden facilitar la toma de decisiones estratégicas. 
                Por ejemplo, en el caso de un propietario de negocio, contar con datos organizados le permite identificar qué categoría de productos o servicios tiene mayor movimiento dentro de su mercado, 
                y así tomar decisiones informadas sobre inversión, promoción o mejora operativa.
                cuando se fundamentan en datos concretos, evaluados y contextualizados, se obtiene una visión más precisa del entorno, lo que permite elegir el camino más adecuado o detectar áreas que requieren ajustes.
                Este enfoque analítico es esencial en el mundo actual, donde la información es 
                abundante pero solo se convierte en conocimiento útil cuando se interpreta correctamente.
